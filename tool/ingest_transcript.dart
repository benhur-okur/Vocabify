class _RawCaption {
  _RawCaption({required this.startMs, required this.endMs, required this.text});
  final int startMs;
  final int endMs;
  final String text;
}

void _writeOutput({
  required String movieId,
  String? seriesId,
  required String title,
  required String youtubeId,
  required List<String> tags,
  required List<_RawCaption> captions,
  required String strategyLabel,
}) {
  final segments = _bucketize(captions);
  if (segments.isEmpty) {
    stderr.writeln('✗ Captions parsed but bucketing produced 0 segments.');
    exit(67);
  }

  final out = <String, dynamic>{
    'movieId': movieId,
    if (seriesId != null) 'seriesId': seriesId,
    'movieTitle': title,
    'youtubeVideoId': youtubeId,
    'topicTags': tags,
    'segments': segments,
  };

  final dir = Directory('assets/transcripts');
  if (!dir.existsSync()) dir.createSync(recursive: true);
  final file = File('assets/transcripts/$movieId.json');
  file.writeAsStringSync(const JsonEncoder.withIndent('  ').convert(out));

  print('✓ Wrote ${segments.length} segments via "$strategyLabel"');
  print('  → ${file.path}');
  if (youtubeId.isEmpty) {
    print('  ! youtubeVideoId is empty; edit the JSON to set one');
    print('    before the app can stream playback for these scenes.');
  }
  print('  → Remember to add this path to `transcriptManifest` in');
  print('    lib/features/subtitle_scene/data/transcript_corpus.dart');
  exit(0);
}

List<Map<String, dynamic>> _bucketize(List<_RawCaption> captions) {
  captions.sort((a, b) => a.startMs.compareTo(b.startMs));
  final segments = <Map<String, dynamic>>[];
  var bucketStart = 0;
  var bucketEnd = 0;
  final bucketText = <String>[];

  void flush() {
    if (bucketText.isEmpty) return;
    final text =
        bucketText.join(' ').replaceAll(RegExp(r'\s+'), ' ').trim();
    if (text.isEmpty) {
      bucketText.clear();
      return;
    }
    final lemmas = Lemmatizer.lemmatize(text);
    segments.add({
      'startMs': bucketStart,
      'endMs': bucketEnd,
      'text': text,
      'lemmas': lemmas,
    });
    bucketText.clear();
  }

  for (final c in captions) {
    if (bucketText.isEmpty) {
      bucketStart = c.startMs;
      bucketEnd = c.endMs;
      bucketText.add(c.text);
      continue;
    }
    final proposedDur = c.endMs - bucketStart;
    if (proposedDur <= _kBucketMaxMs) {
      bucketEnd = c.endMs;
      bucketText.add(c.text);
    } else {
      flush();
      bucketStart = c.startMs;
      bucketEnd = c.endMs;
      bucketText.add(c.text);
    }
    if ((bucketEnd - bucketStart) >= _kBucketMinMs &&
        bucketText.length >= 2) {
      flush();
    }
  }
  flush();
  return segments;
}

String _extractText(XmlElement p) {
  final buf = StringBuffer();
  for (final node in p.descendants) {
    if (node is XmlText) buf.write(node.value);
  }
  return _decodeHtml(buf.toString()).replaceAll(RegExp(r'\s+'), ' ');
}

String _decodeHtml(String s) {
  return s
      .replaceAll('&amp;', '&')
      .replaceAll('&lt;', '<')
      .replaceAll('&gt;', '>')
      .replaceAll('&quot;', '"')
      .replaceAll('&#39;', "'")
      .replaceAll('&nbsp;', ' ');
}

String _forceFormat(String url, String fmt) {
  final uri = Uri.parse(url);
  final qp = Map<String, String>.from(uri.queryParameters);
  qp['fmt'] = fmt;
  return uri.replace(queryParameters: qp).toString();
}

bool _looksLikeVtt(String body) {
  final t = body.trimLeft();
  return t.startsWith('WEBVTT') || RegExp(r'^\ufeff?WEBVTT').hasMatch(t);
}

bool _looksLikeSrt(String body) {
  // SRT blocks: "1\n00:00:01,000 --> 00:00:02,000\nText"
  return RegExp(
    r'^\s*\d+\s*\n\s*\d{2}:\d{2}:\d{2}[,.]\d{3}\s*-->',
    multiLine: true,
  ).hasMatch(body);
}

bool _looksLikeTtml(String body) {
  final t = body.trimLeft();
  return t.startsWith('<') &&
      (t.contains('<tt ') ||
          t.contains('<tt>') ||
          t.contains('xmlns="http://www.w3.org/ns/ttml"'));
}

List<String> _parseTags(String? raw) => (raw ?? '')
    .split(',')
    .map((s) => s.trim())
    .where((s) => s.isNotEmpty)
    .toList();

String _preview(String s) {
  final firstLine = s.split('\n').first;
  final t =
      firstLine.length > 120 ? '${firstLine.substring(0, 120)}…' : firstLine;
  return t.replaceAll('"', r'\"');
}

String _summarize(Object e) {
  final s = e.toString();
  return s.length <= 300 ? s : '${s.substring(0, 300)}…';
}

Map<String, String> _parseFlags(List<String> args) {
  final out = <String, String>{};
  for (var i = 0; i < args.length; i++) {
    final a = args[i];
    if (!a.startsWith('--')) continue;
    final key = a.substring(2);
    if (i + 1 >= args.length || args[i + 1].startsWith('--')) {
      out[key] = '';
      continue;
    }
    out[key] = args[++i];
  }
  return out;
}

