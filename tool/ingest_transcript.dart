Future<void> _runFileMode(Map<String, String> flags, _Log log) async {
  final path = flags['file']!;
  if (flags['movieId'] == null || flags['title'] == null) {
    stderr.writeln('error: --movieId and --title are required in file mode');
    _printUsage();
    exit(64);
  }

  final movieId = flags['movieId']!;
  final title = flags['title']!;
  final seriesId = flags['seriesId'];
  final tags = _parseTags(flags['tags']);
  final youtubeId = flags['youtubeId'] ?? '';

  final file = File(path);
  if (!file.existsSync()) {
    stderr.writeln('✗ File not found: $path');
    exit(69);
  }

  String raw;
  try {
    raw = file.readAsStringSync();
  } catch (e) {
    stderr.writeln('✗ Failed to read $path: ${_summarize(e)}');
    exit(69);
  }

  final lower = path.toLowerCase();
  List<_RawCaption>? captions;
  String parser;

  try {
    if (lower.endsWith('.vtt') || _looksLikeVtt(raw)) {
      captions = _parseVtt(raw);
      parser = 'vtt';
    } else if (lower.endsWith('.srt') || _looksLikeSrt(raw)) {
      captions = _parseSrt(raw);
      parser = 'srt';
    } else if (lower.endsWith('.ttml') ||
        lower.endsWith('.xml') ||
        _looksLikeTtml(raw)) {
      captions = _parseTtml(raw);
      parser = 'ttml';
    } else {
      stderr.writeln(
          '✗ Unrecognized subtitle format. Supported: .vtt, .srt, .ttml, .xml');
      exit(68);
    }
  } catch (e) {
    stderr.writeln('✗ Failed to parse $path: ${_summarize(e)}');
    exit(68);
  }

  if (captions.isEmpty) {
    stderr.writeln('✗ Parsed $path but found 0 captions.');
    exit(68);
  }

  log.info('Parsed ${captions.length} captions using $parser parser.');

  _writeOutput(
    movieId: movieId,
    seriesId: seriesId,
    title: title,
    youtubeId: youtubeId,
    tags: tags,
    captions: captions,
    strategyLabel: 'file:$parser',
  );
}

// ─────────────────────────────────────────────────────────────────────────────
// YouTube candidate selection
// ─────────────────────────────────────────────────────────────────────────────

class _Candidate {
  _Candidate({required this.track, required this.formatOverride});
  final ClosedCaptionTrackInfo track;

  /// null = use the track URL as-is (no fmt rewrite). Otherwise force
  /// a specific fmt query param: 'srv3', 'vtt', 'ttml', 'json3'.
  final String? formatOverride;
}

/// Produces an ordered candidate list. Order of preference:
///   1. Manual (non-auto) tracks matching the exact lang, trying formats
///      in this order: original URL → srv3 → vtt → ttml → json3.
///   2. Manual tracks matching the language root (e.g. "en" matches "en-GB").
///   3. Auto-generated tracks, same language/format preference.
///   4. First track in the manifest, all formats.
List<_Candidate> _buildCandidatePairs(
    ClosedCaptionManifest manifest, String lang) {
  const formatOrder = <String?>[null, 'srv3', 'vtt', 'ttml', 'json3'];

  bool exactLang(ClosedCaptionTrackInfo t) =>
      t.language.code.toLowerCase() == lang.toLowerCase();

  bool rootLang(ClosedCaptionTrackInfo t) {
    final root = lang.toLowerCase().split('-').first;
    final code = t.language.code.toLowerCase();
    return code == root || code.startsWith('$root-');
  }

  final seen = <String>{};
  final out = <_Candidate>[];

  void addGroup(Iterable<ClosedCaptionTrackInfo> tracks) {
    for (final t in tracks) {
      for (final fmt in formatOrder) {
        final key =
            '${t.url}|${fmt ?? "orig"}|${t.language.code}|${t.isAutoGenerated}';
        if (seen.add(key)) {
          out.add(_Candidate(track: t, formatOverride: fmt));
        }
      }
    }
  }

  // Tier A: manual + exact lang
  addGroup(
    manifest.tracks.where((t) => !t.isAutoGenerated && exactLang(t)),
  );
  // Tier B: manual + root lang
  addGroup(
    manifest.tracks.where((t) => !t.isAutoGenerated && rootLang(t)),
  );
  // Tier C: auto + exact lang
  addGroup(
    manifest.tracks.where((t) => t.isAutoGenerated && exactLang(t)),
  );
  // Tier D: auto + root lang
  addGroup(
    manifest.tracks.where((t) => t.isAutoGenerated && rootLang(t)),
  );
  // Tier E: anything
  if (out.isEmpty) addGroup(manifest.tracks);

  return out;
}

Future<ClosedCaptionManifest?> _safeGetManifest(
    YoutubeExplode yt, String videoId, _Log log) async {
  try {
    return await yt.videos.closedCaptions.getManifest(videoId, formats: const [
      ClosedCaptionFormat.srv3,
      ClosedCaptionFormat.ttml,
      ClosedCaptionFormat.vtt,
    ]);
  } catch (_) {
    try {
      return await yt.videos.closedCaptions.getManifest(videoId);
    } catch (e2) {
      stderr.writeln('✗ Failed to get caption manifest: ${_summarize(e2)}');
      return null;
    }
  }
}

// ─────────────────────────────────────────────────────────────────────────────
// Fetch + sniff + parse
// ─────────────────────────────────────────────────────────────────────────────

Future<List<_RawCaption>> _fetchAndParse({
  required http.Client client,
  required ClosedCaptionTrackInfo track,
  required String? formatOverride,
  required _Log log,
}) async {
  final url = formatOverride == null
      ? track.url.toString()
      : _forceFormat(track.url.toString(), formatOverride);

  log.debug('GET $url');

  final resp = await client.get(Uri.parse(url)).timeout(
    const Duration(seconds: 15),
    onTimeout: () {
      throw TimeoutException('timed out after 15s');
    },
  );
  if (resp.statusCode != 200) {
    throw HttpException(
      'HTTP ${resp.statusCode}',
      uri: Uri.parse(url),
    );
  }
  final body = resp.body;
  final trimmed = body.trim();
  if (trimmed.isEmpty) {
    throw const FormatException('response was empty (0 bytes)');
  }

  // Sniff format from body, not from the URL parameter. YouTube sometimes
  // serves one format when we asked for another, or serves nothing when the
  // requested format isn't available for that track.
  if (trimmed.startsWith('<')) {
    // XML family: could be srv3, srv1/srv2, or TTML.
    return _parseXmlTimedText(trimmed);
  } else if (trimmed.startsWith('{')) {
    return _parseJson3(trimmed);
  } else if (_looksLikeVtt(trimmed)) {
    return _parseVtt(trimmed);
  } else {
    throw FormatException(
        'unrecognized body (first bytes: "${_preview(trimmed)}")');
  }
}

// ─────────────────────────────────────────────────────────────────────────────
// Parsers
// ─────────────────────────────────────────────────────────────────────────────

List<_RawCaption> _parseXmlTimedText(String body) {
  final doc = XmlDocument.parse(body);
  final out = <_RawCaption>[];

  // srv3: <timedtext><body><p t="..." d="...">...</p></body></timedtext>
  for (final p in doc.findAllElements('p')) {
    final tAttr = p.getAttribute('t');
    if (tAttr == null) continue;
    final startMs = int.tryParse(tAttr) ?? 0;
    final durMs = int.tryParse(p.getAttribute('d') ?? '') ?? 0;
    final endMs = startMs + (durMs > 0 ? durMs : 3000);
    final text = _extractText(p).trim();
    if (text.isEmpty) continue;
    out.add(_RawCaption(startMs: startMs, endMs: endMs, text: text));
  }

  // srv1/srv2: <transcript><text start="..." dur="...">...</text></transcript>
  if (out.isEmpty) {
    for (final t in doc.findAllElements('text')) {
      final startAttr = t.getAttribute('start');
      if (startAttr == null) continue;
      final startSec = double.tryParse(startAttr) ?? 0;
      final durSec = double.tryParse(t.getAttribute('dur') ?? '') ?? 0;
      final startMs = (startSec * 1000).round();
      final endMs =
          ((startSec + (durSec > 0 ? durSec : 3)) * 1000).round();
      final text = _decodeHtml(t.innerText).trim();
      if (text.isEmpty) continue;
      out.add(_RawCaption(startMs: startMs, endMs: endMs, text: text));
    }
  }

  // TTML: <tt><body><div><p begin="00:00:01.000" end="00:00:03.000">...</p>...
  if (out.isEmpty) {
    for (final p in doc.findAllElements('p')) {
      final begin = p.getAttribute('begin');
      final end = p.getAttribute('end');
      final dur = p.getAttribute('dur');
      if (begin == null) continue;
      final startMs = _ttmlTimeToMs(begin);
      final endMs = end != null
          ? _ttmlTimeToMs(end)
          : (dur != null ? startMs + _ttmlTimeToMs(dur) : startMs + 3000);
      final text = _extractText(p).trim();
      if (text.isEmpty) continue;
      out.add(_RawCaption(startMs: startMs, endMs: endMs, text: text));
    }
  }

  return out;
}

List<_RawCaption> _parseJson3(String body) {
  final data = jsonDecode(body) as Map<String, dynamic>;
  final events = (data['events'] as List<dynamic>?) ?? const [];
  final out = <_RawCaption>[];
  for (final e in events) {
    final ev = e as Map<String, dynamic>;
    final startMs = (ev['tStartMs'] as num?)?.toInt();
    final durMs = (ev['dDurationMs'] as num?)?.toInt();
    if (startMs == null) continue;
    final segs = (ev['segs'] as List<dynamic>?) ?? const [];
    final buf = StringBuffer();
    for (final s in segs) {
      final utf8Text = (s as Map<String, dynamic>)['utf8'];
      if (utf8Text is String) buf.write(utf8Text);
    }
    final text = buf.toString().trim();
    if (text.isEmpty) continue;
    final endMs = startMs + (durMs ?? 3000);
    out.add(_RawCaption(startMs: startMs, endMs: endMs, text: text));
  }
  return out;
}

List<_RawCaption> _parseVtt(String body) {
  // WebVTT block format:
  //   00:00:01.000 --> 00:00:03.500 [optional settings]
  //   line 1
  //   line 2 (may span multiple lines until a blank line)
  final lines = body.replaceAll('\r\n', '\n').split('\n');
  final out = <_RawCaption>[];
  final cueRe = RegExp(
    r'^(\d{1,2}:)?(\d{2}):(\d{2})\.(\d{3})\s*-->\s*(\d{1,2}:)?(\d{2}):(\d{2})\.(\d{3})',
  );

  for (var i = 0; i < lines.length; i++) {
    final line = lines[i];
    final m = cueRe.firstMatch(line);
    if (m == null) continue;

    int toMs(String? h, String mm, String ss, String ms) {
      final hours = int.parse((h ?? '0:').replaceAll(':', ''));
      final mins = int.parse(mm);
      final secs = int.parse(ss);
      final millis = int.parse(ms);
      return (((hours * 60) + mins) * 60 + secs) * 1000 + millis;
    }

    final startMs = toMs(m.group(1), m.group(2)!, m.group(3)!, m.group(4)!);
    final endMs = toMs(m.group(5), m.group(6)!, m.group(7)!, m.group(8)!);

    // Read text lines until blank
    final textBuf = <String>[];
    var j = i + 1;
    while (j < lines.length && lines[j].trim().isNotEmpty) {
      textBuf.add(_stripVttTags(lines[j]));
      j++;
    }
    i = j;

    final text = textBuf.join(' ').replaceAll(RegExp(r'\s+'), ' ').trim();
    if (text.isEmpty) continue;
    out.add(_RawCaption(startMs: startMs, endMs: endMs, text: text));
  }
  return out;
}

String _stripVttTags(String line) {
  // Remove VTT inline tags like <c.color>, <v Speaker>, <00:00:05.000>.
  return line.replaceAll(RegExp(r'<[^>]+>'), '');
}

List<_RawCaption> _parseSrt(String body) {
  final normalized = body.replaceAll('\r\n', '\n');
  // Blocks are separated by blank lines.
  final blocks = normalized.split(RegExp(r'\n{2,}'));
  final timeRe = RegExp(
    r'(\d{2}):(\d{2}):(\d{2})[,.](\d{3})\s*-->\s*(\d{2}):(\d{2}):(\d{2})[,.](\d{3})',
  );
  final out = <_RawCaption>[];
  for (final block in blocks) {
    final lines = block
        .split('\n')
        .map((l) => l.trim())
        .where((l) => l.isNotEmpty)
        .toList();
    if (lines.isEmpty) continue;

    // The index line is optional-ish; find the timestamp line.
    String? timeLine;
    var textStart = 0;
    for (var i = 0; i < lines.length; i++) {
      if (timeRe.hasMatch(lines[i])) {
        timeLine = lines[i];
        textStart = i + 1;
        break;
      }
    }
    if (timeLine == null) continue;
    final m = timeRe.firstMatch(timeLine)!;
    int toMs(int h, int mm, int ss, int ms) =>
        (((h * 60) + mm) * 60 + ss) * 1000 + ms;
    final startMs = toMs(
      int.parse(m.group(1)!),
      int.parse(m.group(2)!),
      int.parse(m.group(3)!),
      int.parse(m.group(4)!),
    );
    final endMs = toMs(
      int.parse(m.group(5)!),
      int.parse(m.group(6)!),
      int.parse(m.group(7)!),
      int.parse(m.group(8)!),
    );
    final text = lines
        .sublist(textStart)
        .join(' ')
        .replaceAll(RegExp(r'<[^>]+>'), '')
        .replaceAll(RegExp(r'\s+'), ' ')
        .trim();
    if (text.isEmpty) continue;
    out.add(_RawCaption(startMs: startMs, endMs: endMs, text: text));
  }
  return out;
}

List<_RawCaption> _parseTtml(String body) {
  final doc = XmlDocument.parse(body);
  final out = <_RawCaption>[];
  for (final p in doc.findAllElements('p')) {
    final begin = p.getAttribute('begin');
    if (begin == null) continue;
    final startMs = _ttmlTimeToMs(begin);
    final end = p.getAttribute('end');
    final dur = p.getAttribute('dur');
    final endMs = end != null
        ? _ttmlTimeToMs(end)
        : (dur != null ? startMs + _ttmlTimeToMs(dur) : startMs + 3000);
    final text = _extractText(p).trim();
    if (text.isEmpty) continue;
    out.add(_RawCaption(startMs: startMs, endMs: endMs, text: text));
  }
  return out;
}

/// Parses TTML timestamp strings: "HH:MM:SS.mmm", "SS.mmms", "SSs", or plain ms.
int _ttmlTimeToMs(String s) {
  final clock =
      RegExp(r'^(\d{1,2}):(\d{2}):(\d{2})(?:\.(\d{1,3}))?$').firstMatch(s);
  if (clock != null) {
    final h = int.parse(clock.group(1)!);
    final m = int.parse(clock.group(2)!);
    final sec = int.parse(clock.group(3)!);
    final ms = int.parse((clock.group(4) ?? '0').padRight(3, '0'));
    return (((h * 60) + m) * 60 + sec) * 1000 + ms;
  }
  final seconds = RegExp(r'^([\d.]+)s$').firstMatch(s);
  if (seconds != null) {
    return (double.parse(seconds.group(1)!) * 1000).round();
  }
  return int.tryParse(s) ?? 0;
}

