// YouTube mode
// ─────────────────────────────────────────────────────────────────────────────

Future<void> _runYoutubeMode(Map<String, String> flags, _Log log) async {
  final videoId = flags['id']!;
  final listOnly = flags.containsKey('list');

  if (!listOnly) {
    if (flags['movieId'] == null || flags['title'] == null) {
      stderr.writeln(
          'error: --movieId and --title are required unless --list');
      _printUsage();
      exit(64);
    }
  }

  final lang = flags['lang'] ?? 'en';
  final movieId = flags['movieId'] ?? '';
  final title = flags['title'] ?? '';
  final seriesId = flags['seriesId'];
  final tags = _parseTags(flags['tags']);

  final yt = YoutubeExplode();
  final client = http.Client();

  try {
    log.info('Fetching caption manifest for video "$videoId"…');
    final manifest = await _safeGetManifest(yt, videoId, log);
    if (manifest == null) exit(70);
    if (manifest.tracks.isEmpty) {
      stderr.writeln(
          '✗ No caption tracks available for $videoId. Publish captions or try a different video.');
      exit(65);
    }

    log.info('Available tracks:');
    for (final t in manifest.tracks) {
      log.info('  • lang=${t.language.code} (${t.language.name}) '
          'auto=${t.isAutoGenerated} format=${t.format.formatCode}');
    }

    if (listOnly) exit(0);

    // Build an ordered candidate list of (track, format) pairs to try.
    final candidates = _buildCandidatePairs(manifest, lang);
    if (candidates.isEmpty) {
      stderr.writeln(
          '✗ No tracks matched language "$lang". Run with --list to inspect, then pass --lang <code>.');
      exit(66);
    }

    log.info('Will try ${candidates.length} (track × format) combinations.');

    final errors = <String>[];
    List<_RawCaption>? captions;
    String? usedStrategy;

    for (final pair in candidates) {
      final strategyName =
          '${pair.track.language.code}/${pair.formatOverride ?? "orig(${pair.track.format.formatCode})"}';
      log.info('→ Trying: $strategyName '
          '${pair.track.isAutoGenerated ? "(auto-gen) " : ""}');

      try {
        final result = await _fetchAndParse(
          client: client,
          track: pair.track,
          formatOverride: pair.formatOverride,
          log: log,
        );
        if (result.isNotEmpty) {
          captions = result;
          usedStrategy = strategyName;
          break;
        }
        errors.add('$strategyName: returned 0 captions');
        log.warn('$strategyName: returned 0 captions');
      } catch (e) {
        errors.add('$strategyName: ${_summarize(e)}');
        log.warn('$strategyName: ${_summarize(e)}');
      }
    }

    // Last-resort: let youtube_explode_dart try on its own against the best track.
    if (captions == null || captions.isEmpty) {
      log.info('→ Last resort: youtube_explode_dart default parser');
      final best = candidates.first.track;
      try {
        final tr = await yt.videos.closedCaptions.get(best);
        final out = <_RawCaption>[];
        for (final c in tr.captions) {
          final t = c.text.trim();
          if (t.isEmpty) continue;
          out.add(_RawCaption(
            startMs: c.offset.inMilliseconds,
            endMs: (c.offset + c.duration).inMilliseconds,
            text: t,
          ));
        }
        if (out.isNotEmpty) {
          captions = out;
          usedStrategy = 'youtube_explode_dart/default';
        } else {
          errors.add('youtube_explode_dart/default: 0 captions');
        }
      } catch (e) {
        errors.add('youtube_explode_dart/default: ${_summarize(e)}');
      }
    }

    if (captions == null || captions.isEmpty) {
      stderr.writeln('✗ All YouTube ingestion strategies failed:');
      for (final e in errors) {
        stderr.writeln('  · $e');
      }
      stderr.writeln('\nHints:');
      stderr.writeln(
          '  · Some videos refuse to serve timedtext to non-browser clients.');
      stderr.writeln(
          '  · Download the .vtt/.srt yourself (e.g. yt-dlp --write-sub --sub-lang $lang --skip-download <videoId>),');
      stderr.writeln(
          '    then re-run this tool with --file <path> instead of --id.');
      exit(67);
    }

    _writeOutput(
      movieId: movieId,
      seriesId: seriesId,
      title: title,
      youtubeId: videoId,
      tags: tags,
      captions: captions,
      strategyLabel: usedStrategy ?? 'unknown',
    );
  } finally {
    yt.close();
    client.close();
  }
}

