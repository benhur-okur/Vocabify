// ignore_for_file: avoid_print, deprecated_member_use
//
// Vocabify transcript ingestion CLI (v3).
//
// Two modes:
//
//  1) YouTube mode — fetch captions directly from YouTube:
//       dart run tool/ingest_transcript.dart \
//           --id <youtubeVideoId> \
//           --movieId <slug> \
//           --title "Movie Title" \
//           --tags travel,daily \
//           [--lang en] [--list] [--debug]
//
//  2) File mode — ingest a local .vtt / .srt / .ttml subtitle file:
//       dart run tool/ingest_transcript.dart \
//           --file path/to/subs.vtt \
//           --movieId <slug> \
//           --title "Movie Title" \
//           --youtubeId <id> \         # optional — used by the in-app player
//           --tags travel,daily \
//           [--debug]
//
// Output: assets/transcripts/<movieId>.json
//
// Exit codes:
//   0  success
//   64 usage error
//   65 no caption tracks available (YouTube mode)
//   66 no track for requested language (YouTube mode)
//   67 all ingestion strategies failed
//   68 local subtitle file could not be parsed
//   69 local subtitle file not found / unreadable
//   70 network error (YouTube mode)
//   99 unexpected error

import 'dart:async';
