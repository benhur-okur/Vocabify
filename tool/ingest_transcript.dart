const _kBucketMaxMs = 10000;
const _kBucketMinMs = 5000;

Future<void> main(List<String> args) async {
  final flags = _parseFlags(args);
  final debug = flags.containsKey('debug');

  if (flags.containsKey('help') || flags.containsKey('h')) {
    _printUsage();
    exit(0);
  }

  final log = _Log(debug);

  try {
    if (flags.containsKey('file')) {
      await _runFileMode(flags, log);
    } else if (flags.containsKey('id')) {
      await _runYoutubeMode(flags, log);
    } else {
      stderr.writeln('error: --id or --file is required');
      _printUsage();
      exit(64);
    }
  } catch (e, st) {
    stderr.writeln('✗ Unexpected error: ${_summarize(e)}');
    if (debug) stderr.writeln(st);
    exit(99);
  }
}

